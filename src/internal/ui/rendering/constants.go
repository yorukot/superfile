package rendering

// Border rendering constants
const (
	// BorderCornerWidth is the width occupied by border corners
	BorderCornerWidth = 2
	
	// BorderPaddingWidth is padding around border title/content
	BorderPaddingWidth = 2
	
	// BorderDividerWidth is width for middle dividers (MiddleLeft + MiddleRight + Bottom)
	BorderDividerWidth = 3
	
	// MinTitleWidth is minimum width needed to render at least 1 char of title
	MinTitleWidth = 5
	
	// MinInfoItemWidth is minimum width for each info item to render at least 1 char
	MinInfoItemWidth = 4
)
